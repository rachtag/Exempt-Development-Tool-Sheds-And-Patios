def patio_check():
    """ This function provides the rule-set for Exempt Development of a Patio under the SEPP
        returning a string indicating if the proposed development is Exempt or if not, returning
        a string indicating reson for Not Exempt under SEPP"""
    
    print("\nü™¥ Patio Development Check\n")

    if attributes["zoning"] not in ["R1", "R2", "R3", "R4", "R5", "RU1", "RU2", "RU3", "RU4", "RU6"]:
        return "‚ùå Zoning is out of scope of this tool - only zones R1-R6 and RU1-RU4 and RU6 are assessable with this tool"

    if attributes["structure_type"] == "replacement":
        if attributes["height_existing"] > 1:
            return "‚ùå Not Exempt: Existing height exceeds 1m. (SEPP, Part 2, Division 1, Subdivision 6, 2.11 (b))"

        if attributes["material_quality"] == "no":
            return "‚ùå Not Exempt: Material quality not met. (SEPP, Part 2, Division 1, Subdivision 6, 2.12 (2)(a))"
    
        if attributes["structure_type"] == "replacement" and attributes["same_size"] == "no":
            return "‚ùå Not Exempt: Replacement not same size. (SEPP, Part 2, Division 1, Subdivision 6, 2.12 (2)(b))"

    if attributes["heritage"] == "yes":
        return "‚ùå Not Exempt: Heritage item. (SEPP, Part 2, Division 1, Subdivision 6, 2.11 (a))"

    if attributes["foreshore"] == "yes":
        return "‚ùå Not Exempt: Foreshore area. (SEPP, Part 2, Division 1, Subdivision 6, 2.11 (a))"

    if attributes["area"] > 25:
        return "‚ùå Not Exempt: Area exceeds limits. (SEPP, Part 2, Division 1, Subdivision 6, 2.12 (1)(b))"
    else:
        if attributes["land_size"] > 300:
            if attributes["total_structures_area"] > 0.15 * attributes["land_size"]:
                return "‚ùå Not Exempt: Area exceeds limits. (SEPP, Part 2, Division 1, Subdivision 6, 2.12 (1)(c)(i))"
        else:
            if attributes["total_structures_area"] > 25:
                return "‚ùå Not Exempt: Area exceeds limits. (SEPP, Part 2, Division 1, Subdivision 6, 2.12 (1)(c)(ii))"

    if attributes["wall_height"] == "yes":
        return "‚ùå Not Exempt: Wall height exceeds limit. (SEPP, Part 2, Division 1, Subdivision 6, 2.12 (1)(d))"

    if attributes["behind_building_line"] == "no":
        return "‚ùå Not Exempt: Not behind building line. (SEPP, Part 2, Division 1, Subdivision 6, 2.12 (1)(e)(ii))"

    if attributes["boundary_distance"] < 900:
        return "‚ùå Not Exempt: Too close to boundary. (SEPP, Part 2, Division 1, Subdivision 6, 2.12 (1)(f)(ii))"
    elif attributes["boundary_distance"] < 5000:
        if attributes["zoning"] not in ["R5", "RU1", "RU2", "RU3", "RU4", "RU6"]:
            return "‚ùå Not Exempt: Too close to boundary. (SEPP, Part 2, Division 1, Subdivision 6, 2.12 (1)(f)(i))" 

    if attributes["metal"] == "yes":
        if attributes["reflective"] == "no":
            return "‚ùå Not Exempt: Metal components not compliant. (SEPP, Part 2, Division 1, Subdivision 6, 2.12 (1)(h))"

    if attributes["floor_height"] > 1000:
        return "‚ùå Not Exempt: Floor height exceeds 1m. (SEPP, Part 2, Division 1, Subdivision 6, 2.12 (1)(i))"

    if attributes["roof"] == "yes":
        if attributes["overhang"] > 600:
            return "‚ùå Not Exempt: Roof overhang exceeds 600mm. (SEPP, Part 2, Division 1, Subdivision 6, 2.12 (1)(i1))"

        if attributes["attached"] == "yes":
            if attributes["above_gutter"] == "yes":
                return "‚ùå Not Exempt: Roof extends above gutter. (SEPP, Part 2, Division 1, Subdivision 6, 2.12 (1)(j))"

            if attributes["roof_height"] > 3:
                return "‚ùå Not Exempt: Roof height exceeds 3m. (SEPP, Part 2, Division 1, Subdivision 6, 2.12 (1)(j1))"

            if attributes["fascia_connection"] == "yes":
                if attributes["engineer_spec"] == "no":
                    return "‚ùå Not Exempt: Fascia connection not compliant. (SEPP, Part 2, Division 1, Subdivision 6, 2.12 (1)(k))"

            if attributes["stormwater"] == "no":
                return "‚ùå Not Exempt: No stormwater disposal. (SEPP, Part 2, Division 1, Subdivision 6, 2.12 (1)(l))"

    if attributes["drainage"] == "yes":
        return "‚ùå Not Exempt: Interferes with drainage. (SEPP, Part 2, Division 1, Subdivision 6, 2.12 (1)(m))"

    if attributes["bushfire"] == "yes":
        if attributes["distance_dwelling"] < 5:
            if attributes["non_combustible"] == "no":
                return "‚ùå Not Exempt: Bushfire material standards not met. (SEPP, Part 2, Division 1, Subdivision 6, 2.12 (1)(n))"

    return "‚úÖ Exempt Development: Patio meets criteria set out in SEPP, Part 2, Division 1, Subdivision 6, 2.11 and 2.12"


def shed_check():
    """ This function provides the rule-set for Exempt Development of a Shed under the SEPP
        returning a string indicating if the proposed development is Exempt or if not, returning
        a string indicating reson for Not Exempt under SEPP"""

    print("\nüîß Shed Development Check\n")

    if attributes["zoning"] not in ["R1", "R2", "R3", "R4", "R5", "RU1", "RU2", "RU3", "RU4", "RU6"]:
        return "‚ùå Zoning is out of scope of this tool - only zones R1-R6 and RU1-RU4 and RU6 are assessable with this tool"

    if attributes["heritage"] == "yes":
        return "‚ùå Not Exempt: Heritage item. (SEPP, Part 2, Division 1, Subdivision 6, 2.17)"

    if attributes["foreshore"] == "yes":
        return "‚ùå Not Exempt: Foreshore area. (SEPP, Part 2, Division 1, Subdivision 6, 2.17)"

    if attributes["sensitive_area"] == "yes":
        return "‚ùå Not Exempt: Environmentally sensitive area. (SEPP, Part 2, Division 1, Subdivision 6, 2.17)"

    if attributes["area"] > 20:
        return "‚ùå Not Exempt: Area exceeds 20m¬≤. (SEPP, Part 2, Division 1, Subdivision 6, 2.18 (1)(b))"

    if attributes["height"] > 3:
        return "‚ùå Not Exempt: Height exceeds 3m. (SEPP, Part 2, Division 1, Subdivision 6, 2.18 (1)(c))"

    if attributes["boundary_distance"] < 900:
        return "‚ùå Not Exempt: Too close to boundary. (SEPP, Part 2, Division 1, Subdivision 6, 2.18 (1)(d)(ii))"
    elif attributes["boundary_distance"] < 5000:
        if attributes["zoning"] not in ["R5", "RU1", "RU2", "RU3", "RU4", "RU6"]:
            return "‚ùå Not Exempt: Too close to boundary. (SEPP, Part 2, Division 1, Subdivision 6, 2.18 (1)(d)(i))" 

    if attributes["building_line"] == "no":
        if attributes["zoning"] not in ["RU1", "RU2", "RU3", "RU4", "RU6"]:
            return "‚ùå Not Exempt: Not behind building line. (SEPP, Part 2, Division 1, Subdivision 6, 2.18 (1)(e))"

    if attributes["shipping_container"] == "yes":
        return "‚ùå Not Exempt: Shipping containers not allowed. (SEPP, Part 2, Division 1, Subdivision 6, 2.18 (1)(f))"

    if attributes["stormwater"] == "no":
        return "‚ùå Not Exempt: Stormwater disposal not compliant. (SEPP, Part 2, Division 1, Subdivision 6, 2.18 (1)(g))"

    if attributes["metal"] == "yes":
        if attributes["reflective"] == "no":
            return "‚ùå Not Exempt: Metal components not compliant. (SEPP, Part 2, Division 1, Subdivision 6, 2.18 (1)(h))"

    if attributes["bushfire"] == "yes":
        if attributes["distance_dwelling"] < 5:
            if attributes["non_combustible"] == "no":
                return "‚ùå Not Exempt: Bushfire standards not met. (SEPP, Part 2, Division 1, Subdivision 6, 2.18 (1)(i))"

    if attributes["adjacent_building"] == "yes":
        if attributes["interfere"] == "yes":
            return "‚ùå Not Exempt: Interferes with building access or safety. (SEPP, Part 2, Division 1, Subdivision 6, 2.18 (1)(k))"

    if attributes["habitable"] == "yes":
        return "‚ùå Not Exempt: Habitable sheds not allowed. (SEPP, Part 2, Division 1, Subdivision 6, 2.18 (1)(l))"

    if attributes["easement"] == "yes":
        return "‚ùå Not Exempt: Too close to easement. (SEPP, Part 2, Division 1, Subdivision 6, 2.18 (1)(m))"

    if attributes["services"] == "yes":
        return "‚ùå Not Exempt: Service connections not allowed. (SEPP, Part 2, Division 1, Subdivision 6, 2.18 (1)(n))"

    if attributes["existing_structures"] == "yes":
        return "‚ùå Not Exempt: Maximum number of structures exceeded. (SEPP, Part 2, Division 1, Subdivision 6, 2.18 (2))"

    return "‚úÖ Exempt Development: Shed meets criteria set out in SEPP, Part 2, Division 1, Subdivision 9, 2.17 and 2.18"


