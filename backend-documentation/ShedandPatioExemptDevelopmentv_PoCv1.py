def get_valid_input(prompt, valid_options):
    """ This function checks input for a string field to make sure it mathes one of ["valid options"]
        Keeps requesting valid input if user does not choose a valid option"""
    
    while True:
        response = input(prompt).strip().lower()
        if response in valid_options:
            return response
        print(f"‚ùå Invalid input. Please enter one of: {', '.join(valid_options)}")


def get_valid_number(prompt, number_type=float, min_value=None, max_value=None):
    """ This function checks input for a number field optionally within valid limits min_value and max_value 
        Keeps requesting valid input if user does not choose a valid option"""
    
    while True:
        try:
            value = number_type(input(prompt).strip())
            if (min_value is not None and value < min_value) or \
               (max_value is not None and value > max_value):
                print(f"‚ùå Value must be between {min_value} and {max_value}.")
                continue
            return value
        except ValueError:
            print("‚ùå Invalid input. Please enter a valid number.")


def patio_check():
    """ This function provides the rule-set for Exempt Development of a Patio under the SEPP
        returning a string indicating if the proposed development is Exempt or if not, returning
        a string indicating reson for Not Exempt under SEPP"""
    
    print("\nü™¥ Patio Development Check\n")

    zoning = input("In what zoninig is the property? (R1, R2, R3, R4, R5, RU1, RU2, RU3, RU4, RU6): ").strip().upper()
    if zoning not in ["R1", "R2", "R3", "R4", "R5", "RU1", "RU2", "RU3", "RU4", "RU6"]:
        return "‚ùå Zoning is out of scope of this tool - only zones R1-R6 and RU1-RU4 and RU6 are assessable with this tool"

    structure_type = get_valid_input("Is this a new structure or a replacement? (new/replacement): ",["new","replacement"]).strip().lower()
    if structure_type == "replacement":
        height_existing = float(get_valid_number("Height of existing structure from ground level (in meters): "))
        if height_existing > 1:
            return "‚ùå Not Exempt: Existing height exceeds 1m. (SEPP, Part 2, Division 1, Subdivision 6, 2.11 (b))"

        material_quality = get_valid_input("Will the structure use equivalent or better quality materials? (yes/no): ",["yes","no"]).strip().lower()
        if material_quality == "no":
            return "‚ùå Not Exempt: Material quality not met. (SEPP, Part 2, Division 1, Subdivision 6, 2.12 (2)(a))"
    
        same_size = get_valid_input("Will the structure be the same height and size as existing? (yes/no): ",["yes","no"]).strip().lower()
        if structure_type == "replacement" and same_size == "no":
            return "‚ùå Not Exempt: Replacement not same size. (SEPP, Part 2, Division 1, Subdivision 6, 2.12 (2)(b))"

    heritage = get_valid_input("Is the property a heritage item? (yes/no): ",["yes","no"]).strip().lower()
    if heritage == "yes":
        return "‚ùå Not Exempt: Heritage item. (SEPP, Part 2, Division 1, Subdivision 6, 2.11 (a))"

    foreshore = get_valid_input("Is the property in a foreshore area? (yes/no): ",["yes","no"]).strip().lower()
    if foreshore == "yes":
        return "‚ùå Not Exempt: Foreshore area. (SEPP, Part 2, Division 1, Subdivision 6, 2.11 (a))"

    area = float(get_valid_number("Planned area of structure (in m¬≤): ",min_value=0))
    if area > 25:
        return "‚ùå Not Exempt: Area exceeds limits. (SEPP, Part 2, Division 1, Subdivision 6, 2.12 (1)(b))"
    else:
        land_size = float(get_valid_number("Land size (in m¬≤): ",min_value=0))
        total_structures_area = float(get_valid_number("Total area of planned + existing structures [Balconies, decks, patios, pergolas, terraces and verandahs] (in m¬≤): ",min_value=0))
        if land_size > 300:
            if total_structures_area > 0.15 * land_size:
                return "‚ùå Not Exempt: Area exceeds limits. (SEPP, Part 2, Division 1, Subdivision 6, 2.12 (1)(c)(i))"
        else:
            if total_structures_area > 25:
                return "‚ùå Not Exempt: Area exceeds limits. (SEPP, Part 2, Division 1, Subdivision 6, 2.12 (1)(c)(ii))"

    wall_height = get_valid_input("Will any wall exceed 1.4m in height? (yes/no): ",["yes","no"]).strip().lower()
    if wall_height == "yes":
        return "‚ùå Not Exempt: Wall height exceeds limit. (SEPP, Part 2, Division 1, Subdivision 6, 2.12 (1)(d))"

    behind_building_line = get_valid_input("Is the structure behind the building line of road frontage? (yes/no): ",["yes","no"]).strip().lower()
    if behind_building_line == "no":
        return "‚ùå Not Exempt: Not behind building line. (SEPP, Part 2, Division 1, Subdivision 6, 2.12 (1)(e)(ii))"

    boundary_distance = float(get_valid_number("Distance from site boundary (in mm): ",min_value=0))
    if boundary_distance < 900:
        return "‚ùå Not Exempt: Too close to boundary. (SEPP, Part 2, Division 1, Subdivision 6, 2.12 (1)(f)(ii))"
    elif boundary_distance < 5000:
        if zoning not in ["R5", "RU1", "RU2", "RU3", "RU4", "RU6"]:
            return "‚ùå Not Exempt: Too close to boundary. (SEPP, Part 2, Division 1, Subdivision 6, 2.12 (1)(f)(i))" 

    metal = get_valid_input("Will the structure use metal components? (yes/no): ",["yes","no"]).strip().lower()
    if metal == "yes":
        reflective = get_valid_input("Are metal components non-reflective or factory coloured? (yes/no): ",["yes","no"]).strip().lower()
        if reflective == "no":
            return "‚ùå Not Exempt: Metal components not compliant. (SEPP, Part 2, Division 1, Subdivision 6, 2.12 (1)(h))"

    floor_height = float(get_valid_number("Floor height from ground level (in mm): ",min_value=0))
    if floor_height > 1000:
        return "‚ùå Not Exempt: Floor height exceeds 1m. (SEPP, Part 2, Division 1, Subdivision 6, 2.12 (1)(i))"

    roof = get_valid_input("Will the structure have a roof? (yes/no): ",["yes","no"]).strip().lower()
    if roof == "yes":
        overhang = float(get_valid_number("Roof overhang on any side (in mm): ",min_value=0))
        if overhang > 600:
            return "‚ùå Not Exempt: Roof overhang exceeds 600mm. (SEPP, Part 2, Division 1, Subdivision 6, 2.12 (1)(i1))"

        attached = get_valid_input("Will the roof be attached to the dwelling? (yes/no): ",["yes","no"]).strip().lower()
        if attached == "yes":
            above_gutter = get_valid_input("Will the roof extend above the gutter line? (yes/no): ",["yes","no"]).strip().lower()
            if above_gutter == "yes":
                return "‚ùå Not Exempt: Roof extends above gutter. (SEPP, Part 2, Division 1, Subdivision 6, 2.12 (1)(j))"

            roof_height = float(get_valid_number("Roof height above ground level (in meters): ",min_value=0))
            if roof_height > 3:
                return "‚ùå Not Exempt: Roof height exceeds 3m. (SEPP, Part 2, Division 1, Subdivision 6, 2.12 (1)(j1))"

            fascia_connection = get_valid_input("Will the roof be connected to fascia? (yes/no): ",["yes","no"]).strip().lower()
            if fascia_connection == "yes":
                engineer_spec = get_valid_input("Will it be connected per engineer's specs? (yes/no): ",["yes","no"]).strip().lower()
                if engineer_spec == "no":
                    return "‚ùå Not Exempt: Fascia connection not compliant. (SEPP, Part 2, Division 1, Subdivision 6, 2.12 (1)(k))"

            stormwater = get_valid_input("Will roofwater be disposed into existing stormwater system? (yes/no): ",["yes","no"]).strip().lower()
            if stormwater == "no":
                return "‚ùå Not Exempt: No stormwater disposal. (SEPP, Part 2, Division 1, Subdivision 6, 2.12 (1)(l))"

    drainage = get_valid_input("Will the structure interfere with existing drainage or flow paths? (yes/no): ",["yes","no"]).strip().lower()
    if drainage == "yes":
        return "‚ùå Not Exempt: Interferes with drainage. (SEPP, Part 2, Division 1, Subdivision 6, 2.12 (1)(m))"

    bushfire = get_valid_input("Is the property on bushfire prone land? (yes/no): ",["yes","no"]).strip().lower()
    if bushfire == "yes":
        distance_dwelling = float(get_valid_number("Distance from dwelling (in meters): ",min_value=0))
        if distance_dwelling < 5:
            non_combustible = get_valid_input("Will the patio be constructed of non-combustible materials? (yes/no): ",["yes","no"]).strip().lower()
            if non_combustible == "no":
                return "‚ùå Not Exempt: Bushfire material standards not met. (SEPP, Part 2, Division 1, Subdivision 6, 2.12 (1)(n))"

    return "‚úÖ Exempt Development: Patio meets criteria set out in SEPP, Part 2, Division 1, Subdivision 6, 2.11 and 2.12"


